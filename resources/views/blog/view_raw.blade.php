                                <h1>Trump, Ibn Battuta, and The Future of Muslim Travel by Hadi Shakuur</h1>
                                <h4>How one Start-up Seeks to Change the Way Muslims Travel and Live.</h4>
                                <p><a href="javascript:;">Traveling while Muslim</a> can be complicated. Let’s face it — in the current climate, Muslims are subjected to a barrage of stereotypes and sometimes even harassment. When we travel we represent our religion and way of life (and all its baggage). Travelers of all kinds want to feel welcome and connected with the community — and Muslims are no different.</p>
                                <p>Besides the threat of <a href="javascript:;">Islamophobia</a>, we Muslims have particular things to consider when we travel, like:</p>
                                <ul>
                                    <li>Where to offer Prayer,</li>
                                    <li>Where to find Halal food,</li>
                                    <li>Finding religiously acceptable events/activities, and finally</li>
                                    <li>Locating Muslim friendly lodging.</li>
                                </ul>
                                <p>Thinking about where you will get your snooze on should be the least of your worries. Unfortunately, it’s not. See here, here, and here.</p>
                                <p><img src="{{url('')}}/style/assets/img/blog-photo.jpg" alt="Blog" /></p>
                                <p><i>“Now is the perfect time for Muzbnb. We owe it to ourselves, Muslims and other people of faith and moral conviction to open our hearts and our homes with Muzbnb. This venture is on the ball. I am listing my vacation home in Barbados on Muzbnb. Remember your either part of the solution or you’re part of the problem. Muzbnb you’ve got my full support!”</i> — Imam Johari Abdul-Malik, Outreach Director of Dar al-Hijra Islamic Center of Northern Virginia</p>
                                <p>I highly believe that for every issue we face there is a solution. Over the past few months I have been working diligently on a project with two close companions of mine to solve this problem. This project is something that we hope will alleviate the undue discrimination Muslims have faced when traveling, and also allow Muslims to travel and explore freely like the legendary Ibn Battuta, whose curiosity led him to travel during a time when few had the means or the courage.</p>
                                <p>Introducing the Muslim homestay experience of the future: Muzbnb!</p>
